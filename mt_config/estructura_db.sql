CREATE TABLE mt_colecciones(
	id INT(11) NOT NULL AUTO_INCREMENT,
	nombre VARCHAR(70) NOT NULL,
	url VARCHAR(70) NOT NULL,
	descrip VARCHAR(70) NOT NULL,
	tipo INT(1) NOT NULL DEFAULT 1,
