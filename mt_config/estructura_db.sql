	portada VARCHAR(250) NOT NULL,
	usuario INT(11) NOT NULL DEFAULT 1,
	estado INT(1) NOT NULL DEFAULT 0,
	tipo INT(2) NOT NULL DEFAULT 1,
	plantilla VARCHAR(30) NOT NULL,
